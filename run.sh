#!/bin/bash -e

trap 'killall' INT

killall() {
    trap '' INT TERM
    echo
    echo "shutting down..."
    kill -TERM 0
    wait
    echo "done!"
}

echo "[1] watching the js for changes..."
npm run building &

echo "[2] running the server..."
lein ring server-headless &

wait

